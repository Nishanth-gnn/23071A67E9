def add_to_dict(d, key, value):
    d[key] = value

my_dict = {}

# Adding five key-value pairs
add_to_dict(my_dict, 'name', 'Alice')
add_to_dict(my_dict, 'age', 30)
add_to_dict(my_dict, 'city', 'New York')
add_to_dict(my_dict, 'job', 'Engineer')
add_to_dict(my_dict, 'hobby', 'Reading')

print("Dictionary:", my_dict)

